function toRed() {
    var title = document.getElementById('title');
    if (title.style.color == 'red') {
        title.style.color = 'black';
    } else {
        title.style.color = 'red'
    }
}

function toBlue() {
    var title = document.getElementById('title');
    if (title.style.color == 'blue') {
        title.style.color = 'black';
    } else {
        title.style.color = 'blue'
    }
}